/**
 * Get icon SVG HTML by Lucide icon name (kebab-case).
 * Returns empty string if the icon name is not found.
 */
export function getIconByName(name: string, size = 16): string {
  const svg = iconByName[name];
  if (!svg) return '';
  return transformSvg(svg, { size });
}

export interface IconOptions {
  size?: number;
  class?: string | string[];
  strokeWidth?: number;
  color?: string;
  fill?: string;
}

/**
 * Transform an SVG string with custom attributes
 */
function transformSvg(svg: string, options: IconOptions = {}): string {
  const {
    size = 16,
    class: className,
    strokeWidth = 2,
    color,
    fill,
  } = options;

  let result = svg;

  // Set size
  result = result.replace(/width="[^"]*"/, `width="${size}"`);
  result = result.replace(/height="[^"]*"/, `height="${size}"`);

  // Set stroke-width
  result = result.replace(/stroke-width="[^"]*"/, `stroke-width="${strokeWidth}"`);

  // Set color (stroke)
  if (color) {
    result = result.replace(/stroke="[^"]*"/, `stroke="${color}"`);
  }

  // Set fill
  if (fill) {
    result = result.replace(/fill="[^"]*"/, `fill="${fill}"`);
  }

  // Add class
  if (className) {
    const classStr = Array.isArray(className) ? className.join(' ') : className;
    result = result.replace(/<svg/, `<svg class="${classStr}"`);
  }

  return result;
}

/**
 * Get icon HTML with custom options
 */
export function iconHtml(svg: string, options: IconOptions = {}): string {
  return transformSvg(svg, options);
}

/**
 * Get icon with specific size (shorthand)
 */
export function getIcon(svg: string, size = 16, className?: string): string {
  return transformSvg(svg, { size, class: className });
}

